% --- Executes on button press in pushbutton1.
function pushbutton1_Callback(hObject, eventdata, handles)
% hObject    handle to pushbutton1 (see GCBO)
% eventdata  reserved - to be defined in a future version of MATLAB
% handles    structure with handles and user data (see GUIDATA)
global I_rgb;
global label;
axes(handles.axes1);
h1 = impoly(gca,'Closed',false);
foresub = getPosition(h1);
foregroundInd = floor(sub2ind(size(I_rgb),foresub(:,2),foresub(:,1)));
setColor(h1,'red');
h2 = impoly(gca,'Closed',false);
backsub = getPosition(h2);
backgroundInd = floor(sub2ind(size(I_rgb),backsub(:,2),backsub(:,1)));

BW = lazysnapping(I_rgb,label,foregroundInd,backgroundInd);
maskedImg = I_rgb;
maskedImg(repmat(~BW,[1,1,3])) = 0;
axes(handles.axes4);
imshow(maskedImg);

